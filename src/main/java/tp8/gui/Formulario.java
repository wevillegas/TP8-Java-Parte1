    private void validarInformacion() {
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        String edad = txtEdad.getText();
        String departamento = txtDepartamento.getText();

        if (nombres.isEmpty() || apellidos.isEmpty() || edad.isEmpty() || departamento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete toda la informaci칩n.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String mensaje = String.format("Bienvenido %s %s, usted tiene %s a침os y vive en %s.", nombres, apellidos, edad, departamento);
            JOptionPane.showMessageDialog(this, mensaje, "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void limpiarCampos() {
        txtNombres.setText("");
        txtApellidos.setText("");
        txtEdad.setText("");
        txtDepartamento.setText("");
    }

